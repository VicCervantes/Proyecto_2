    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        n_producto = TF1.getText();
        fecha_caducidad = TF2.getText();
        try {
            n_lote = Integer.parseInt(TF3.getText());
        } catch (NumberFormatException e) {
            // handle the exception, e.g., show a message dialog
            System.err.println("Invalid number format for n_lote");
        }
        f_envasado = TF4.getText();
        pais_origen = (String) TF5.getSelectedItem();
        
        JOptionPane.showMessageDialog(this, "Se registro el producto: " + n_producto);
        
        TF1.setText("");
        TF2.setText("");
        TF3.setText("");
        TF4.setText("");
        TF5.setSelectedItem("");
        
        TA.setText("");
        TA.append(n_producto + "\n");
        TA.append(fecha_caducidad + "\n");
        TA.append(n_lote + "\n");
        TA.append(f_envasado + "\n");
        TA.append(pais_origen + "\n");
    }//GEN-LAST:event_jButton2ActionPerformed

